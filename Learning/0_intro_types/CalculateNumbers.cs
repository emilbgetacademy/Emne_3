namespace _0_intro_types
{
    internal class CalculateNumbers
    {
        public static double IntMultiplyWith10(int number)
        {
            double result = (double)number * 10;
            return result;
        }

        public static double IntDivideBy10(int number)
        {
            double result = (double)number / 10;
            return result;
        }
    }
}
